
public class ArithmeticOpr {
    public static void main(String[] args) {
        int p = 20, q = 10;

        System.out.println("Addition = " + (p + q));
        System.out.println(p - q);
        System.out.println(p * q);
        System.out.println(p / q);
        System.out.println(p % q);
    }
}
