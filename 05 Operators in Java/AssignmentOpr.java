
public class AssignmentOpr {
    public static void main(String[] args) {
        int p = 10;
        int q = p;


        System.out.println(p = q);
        System.out.println(p += q);
        System.out.println(p -= q);
        System.out.println(p *= q);
        System.out.println(p /= q);
        System.out.println(p %= q);
    }
}
